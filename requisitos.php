      <h2 style="text-align:center;">Requisitos para doação de sangue</h2>

      <h3>Requisitos básicos</h3>
      <ul>
        <li><i class="fa fa-check"></i> Estar em boas condições de saúde.</li>
        <li><i class="fa fa-check"></i> Ter entre 16 e 69 anos, desde que a primeira doação tenha sido feita até 60 anos.</li>
        <li><i class="fa fa-check"></i> Pesar no mínimo 50kg.</li>
        <li><i class="fa fa-check"></i> Estar descansado (ter dormido pelo menos 6 horas nas últimas 24 horas).</li>
        <li><i class="fa fa-check"></i> Estar alimentado (evitar alimentação gordurosa nas 4 horas que antecedem a doação).</li>
        <li><i class="fa fa-check"></i> Apresentar documento original com foto recente, que permita a identificação do candidato, emitido por órgão oficial.</li>
      </ul>

      <h3>Principais impedimentos temporários</h3>
      <ul>
        <li><i class="fa fa-exclamation"></i> Resfriado: aguardar 7 dias após desaparecimento dos sintomas.</li>
        <li><i class="fa fa-exclamation"></i> Gravidez.</li>
        <li><i class="fa fa-exclamation"></i> 90 dias após parto normal e 180 dias após cesariana.</li>
        <li><i class="fa fa-exclamation"></i> Amamentação (se o parto ocorreu há menos de 12 meses).</li>
        <li><i class="fa fa-exclamation"></i> Ingestão de bebida alcoólica nas 12 horas que antecedem a doação.</li>
        <li><i class="fa fa-exclamation"></i> Tatuagem, maquiagem definitiva e micropigmentação (sobrancelhas, lábios, etc.): aguardar 12 meses.</li>
        <li><i class="fa fa-exclamation"></i> Situações nas quais há maior risco de adquirir doenças sexualmente transmissíveis: aguardar 6 meses.</li>
        <li><i class="fa fa-exclamation"></i> Qualquer procedimento endoscópico (endoscopia digestiva alta, colonoscopia, rinoscopia, etc.): aguardar 6 meses.</li>
        <li><i class="fa fa-exclamation"></i> Extração dentária (verificar uso de medicação) ou tratamento de canal (verificar medicação): por 7 dias.</li>
        <li><i class="fa fa-exclamation"></i> Cirurgia odontológica com anestesia geral: por 4 semanas.</li>
        <li><i class="fa fa-exclamation"></i> Acupuntura: com material descartável/agulha própria, aguardar 24 horas; em procedimento não seguro, aguardar 6 meses; se realizada com laser ou sementes: apto.</li>
        <li><i class="fa fa-exclamation"></i> Vacina contra gripe: por 48 horas.</li>
        <li><i class="fa fa-exclamation"></i> Vacina contra Coronarivus: 48 horas após cada dose (Coronavac e Covaxin), 7 dias após cada dose (AstraZeneca, Pfizer, Janssen-Cilag e Moderna).</li>
        <li><i class="fa fa-exclamation"></i> Viagem ao exterior para os Estados Unidos ou regiões com surto de Febre Amarela impede a doação por 30 dias, após o retorno.</li>
        <li><i class="fa fa-exclamation"></i> Viagem no Brasil: estados como Acre, Amapá, Amazonas, Rondônia, Roraima, Maranhão, Mato Grosso, Pará e Tocantins são locais onde há alta prevalência de malária. Quem esteve nesses estados deve aguardar 12 meses para doar, após o retorno.</li>
      </ul>

      <h3>Principais impedimentos definitivos</h3>
      <ul>
        <li><i class="fa fa-minus"></i> Hepatite após os 11 anos de idade.</li>
        <li><i class="fa fa-minus"></i> Evidência clínica ou laboratorial das seguintes doenças infecciosas transmissíveis pelo sangue: Hepatites B e C, AIDS (vírus HIV), doenças associadas aos vírus HTLV I e II e Doença de Chagas.</li>
        <li><i class="fa fa-minus"></i> Uso de drogas ilícitas injetáveis.</li>
        <li><i class="fa fa-minus"></i> Malária.</li>
        <li><i class="fa fa-minus"></i> Doença de Parkinson.</li>
      </ul>
    </div>

